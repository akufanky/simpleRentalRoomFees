    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        String name = tfName.getText();
        String email = tfEmail.getText();
        String phoneNumber = tfPNumber.getText();
        String room = cbRType.getSelectedItem().toString();
        String price = tfPrice.getText();
        
        int result = JOptionPane.showConfirmDialog(null, "Do you want to confirm this data?","Confirm", JOptionPane.YES_NO_OPTION);
        
        if (result == JOptionPane.YES_OPTION){
            JOptionPane.showMessageDialog(null,
                    "Name\t\t: " + name 
                    + "\nEmail\t\t: " + email 
                    + "\nPhone Number\t: " + phoneNumber
                    + "\nRoom\t\t: " + room
                    + "\nPrice\t\t: " + price);
        } else {
            JOptionPane.showMessageDialog(null, "Sorry, your data was not confirmedðŸ˜¢");
        }
    }//GEN-LAST:event_btnConfirmActionPerformed

    private void cbRTypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbRTypeActionPerformed
        String room = cbRType.getSelectedItem().toString();
        
        switch (room){
            case "Standard Room" -> {
                tfPrice.setEditable(false);
                tfPrice.setText("Rp. 2.000.000,-");
            }
            case "Superior Room" -> {
                tfPrice.setEditable(false);
                tfPrice.setText("Rp. 3.000.000,-");
            }
            case "Deluxe Room" -> {
                tfPrice.setEditable(false);
                tfPrice.setText("Rp. 5.000.000,-");
            }
            case "Family Room" -> {
                tfPrice.setEditable(false);
                tfPrice.setText("Rp. 3.500.000,-");
            }
            default -> {
                tfPrice.setEditable(false);
                tfPrice.setText("");
            }
        }
    }//GEN-LAST:event_cbRTypeActionPerformed

